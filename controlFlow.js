// Control Flow includes if statements and switch statements

// If Statements use the keywords if, else if, and else
const x = 10
const y = 20 
const z = 30

if (z > x) {console.log('30 is greater than 10')}
else if (y < z) {console.log('20 is less than 30')}
else if (y + x === z) {console.log('20 + 10 is equal to 30')}
else {console.log('None of these statements are true')}